package main.utils;

/**
 * Helpful util class to organize all the available colors in one place.
 *
 * Used by the maze to determine which paths it can take.
 */
public enum Color {

    /**
     * Represents the color pink
     */
    PINK,

    /**
     * Represents the color green
     */
    GREEN,

    /**
     * Represents the color black
     */
    BLACK,

    /**
     * Represents the color orange
     */
    ORANGE,

    /**
     * Represents the color blue (currently only used for FINISH)
     */
    BLUE
}
